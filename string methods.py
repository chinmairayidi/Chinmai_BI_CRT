str='lython'
print(str.capitalize()) #Converts the first character of the string to uppercase and the rest to lowercase. 
print(str.title())
print(str.casefold()) #Converts the string to lowercase (more aggressive than lower()).
print(str.startswith('p'))
print(str.find('0'))
print("hi".center(10,"*")) #Centers the string with padding.
print(str.encode()) #Encodes the string using the specified encoding.
